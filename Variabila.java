public class Variabila {
    private Integer index;
    private Integer registru;

    public Variabila(int index, int registru) {
        this.index = index;
        this.registru = registru;
    }

    public Integer getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public Integer getRegistru() {
        return registru;
    }

    public void setRegistru(int registru) {
        this.registru = registru;
    }
}
